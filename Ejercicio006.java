/*
Mostrar si un numero termina en 5 o no
*/
public class Ejercicio006 {
    public static void main(String arg[]){
        int x=35;
        
        if(x%10==5)
            System.out.println("El numero "+x+" termina en 5");
        else
            System.out.println("El numero "+x+" no termina en 5");
            
        
    }
    
}
